// Write a Program to print 1 to 10 using a while loop.
#include <stdio.h>
int main(){
    int num=1;        //initialize a value
    while (num<=10)   //give condition(1<10 it means loop run 10 time)
    {
        printf("%d ",num); //print numbers  from 1 to 10
        num++;    //increment
    }

    return 0;

}